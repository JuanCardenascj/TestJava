public class VariablesPrimitivas {
    public static void VariablesPrimitivas(String[] args){
        /* TYPES OF VARIABLES OR PRIMITIVE DATA*/
        //INTEGERS
        byte age = 28; //V.TYPE OF DATA BYTE(-128 A 128)
        int numTop = 1000; //V.TYPE OF DATA INT(-2M A 2M)
        short numLow = 2;
        ; //V:TYPE OF DATA SHORT (-32768 A 32768)
        long numStop = 234567; //V.TYPE OF DATA LONG (9 0 16)

        //DECIMALS
        float ages = 15.5f; //V:TYPE OF DATA FLOAT(00.0F)
        double agge = 15.6f;
        ; //V.TYPE OF DATA DOUBLE (00.0F)

        //BOOLEANS
        boolean isActive = true; //V:TYPE OF DATA BOOLEAN
        boolean sheActive = false; //V.TYPE OF DATA BOOLEAN

        //CHARS
        char myGrade = 'A'; //V.TYPE OF DATA CHAR.! IN JAVA, SIMPLE QUOTES ARE USED


        System.out.println(age);//(SOUT)ENTER
        System.out.println(numStop);//(SOUT)ENTER
        System.out.println(numLow);//(SOUT)ENTER
        System.out.println(numTop);//(SOUT)ENTER
        System.out.println(ages);//(SOUT)ENTER
        System.out.println(agge);//(SOUT)ENTER
        System.out.println(isActive);//(SOUT)ENTER
        System.out.println(sheActive);//(SOUT)ENTER
    }
}
